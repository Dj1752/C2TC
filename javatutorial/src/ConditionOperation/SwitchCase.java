  package ConditionOperation;

public class SwitchCase {

	public static void main(String[] args) {
		int dayOfWeek = 3;
		switch(dayOfWeek) {
		case 1:
			System.out.println("i am on day break");
			break;
		case 2:
			System.out.println("i am in office");
			break;
		case 3:
			System.out.println("i am playing football");
			break;
		case 4:
			System.out.println("i am on day break");
			break;
			default:
				System.out.println("none the above");
		}
		

	}

}
